#include <iostream>
using namespace std;

int max(int a, int b) {
    // найти наибольшее из двух чисел
    if (a > b) {
        return a;
    }
    else {
        return b;
    }
}


int absolute_diff(int n) {
    // найти абсолютную разницу между числом n и числом 51. Если число больше 51, вернуть утроенную разницу
    const int x = 51;
    if (n > x) {
        return (n - x) * 3;
    }
    else {
        return x - n;
    }
}

